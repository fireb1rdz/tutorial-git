def adicao(n1: int, n2: int) -> int:
    """Somar dois números

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        Soma dos números.
    """
    return n1 + n2


def subtracao(n1: int, n2: int) -> int:
    """Subtrair um número por outro

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        A subtração dos números.
    """
    return n1 - n2


def multiplicacao(n1: int, n2: int) -> int:
    """Realiza a multiplicação dos números

    Args:
        n1: primeiro número.
        n2: segundo número.

    Returns:
        O produto dos números.
    """
    return n1 * n2

